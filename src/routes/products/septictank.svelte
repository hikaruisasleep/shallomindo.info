    <h2>Septic Tank</h2>
    <p>
        Septik Tank adalah tangki penampung sekaligus pengolahan limbah domestik berupa kotoran manusia yang ramah lingkungan.
        <br>
        Adalah bakteri yang ada didalam setiap tangki yang bekerja untuk mengolah kotoran yang masuk.
        <br>
        Sistem kompartemen atau sekat yang ada dalam setiap tangki didesain dengan baik untuk setiap tahapan proses dan juga untuk mencegah bau balik kembali ke kamar mandi. 
        <br>
        Bahan Septik Tank kami terbuat dari Fiberglass dengan ketebalan yang mulai dari 5 mm sehingga awet & tahan pecah.
        <br>
        Cara pemasangan yang mudah & lebih cepat ketimbang septik tank tradisional yang terbuat dari beton.
    </p>
    <p>
        Harga sesuai ukuran, mulai dari 500 liter (0.5 m3/kubik) sampai dgn 3000 liter (3 m3/kubik).
        <br>
        Garansi bebas sedot WC sampai dengan 30 tahun (tipe paling besar).
    </p>
