<div class="container">
    <h2>BIO 2000</h2>
    <p>
        BIO 2000 adalah Formula Penguras WC hasil pengembangan bioteknologi yang dibuat dari bahan-bahan alami dengan bahan aktif 15% serbuk inokulan yang ramah lingkungan.
        <br>
        BIO 2000 berisi Bakteri organik dalam bentuk bubuk yang sangat efektif berguna sebagai pengurai limbah feses atau tinja untuk mencegah septik tank cepat penuh & mengatasi saluran yang mampat yang disebabkan oleh limbah organik serta menghilangkan bau tidak sedap tanpa merusak saluran/pipa dan ramah lingkungan.
    </p>
    <div class="box fungsi">
        <p>
            Fungsi BIO 2000 adalah sebagai berikut:
        </p>
        <ol>
            <li><b>Mencegah WC penuh</b>, BIO 2000 merupakan starter bakteri pengurai (bifido bacteria) yang mempunyai kemampuan mendegradasi/mengurai limbah kotoran manusia, sehingga sangat baik digunakan untuk perawatan septic tank agar tidak cepat penuh.</li>
            <li><b>Menghilangkan bau tidak sedap</b>, mampu menyerap bau-bauan.</li>
            <li><b>Tidak merusak pipa saluran pembuangan</b>, aman bagi pipa karena tidak mengandung bahan kimia yang mengakibatkan korosi pada pipa saluran pembuangan.</li>
            <li><b>Ramah lingkungan</b> karena terbuat dari bahan organik, sehingga aman bagi lingkungan, serta mampu mengurangi pencemaran lingkungan yang disebabkan oleh bakteri e-coli, karena mengandung bakteri baik (bakteri selulolitik, proteolitik dan amilolitik) yang dapat mematikan bakteri e-coli.</li>
            <li><b>Praktis</b>, mudah dalam penggunaannya.</li>
            <li><b>Hemat</b> karena lebih murah ketimbang biaya penyedotan tinja.</li>
        </ol>
    </div>
    <div class="box pemakaian">
        <p>
            Cara penggunaannya sebagai berikut :
        </p>
        <ol>
            <li>Masukkan 1 bungkus BIO 2000 ke dalam lobang WC atau kloset yang mampat, kemudian siram dengan air secukupnya sehingga bersih.</li>
            <li>Hasil akan mulai terlihat setelah 3 - 5 jam dan volume tinja akan berkurang 75%, akan lebih effektif bila dibiarkan hingga semalaman.</li>
            <li>Kloset dapat dipakai kembali seperti semula.</li>
        </ol>
    </div>
    <p class="small">Tersedia dalam kemasan 500 gram</p>
</div>
