    <h3>Wood Plastic Composite</h3>
    <p>Wood Plastic Composite adalah Solusi terbaik pengganti kayu, terbuat dari material yang terdiri dari komposisi 60% serbuk kayu jati, 30% Plastik HDPE Daur Ulang & 10% Bahan kimia yang ramah lingkungan. 
        Keunggulan WPC adalah sebagai berikut :
        Anti Jamur
        Anti Rayap
        Tahan Air 
        Anti Oksidan
        Anti UV (Ultra Violet)
        Tidak menjalarkan api
        Mudah dalam pemasangan
        Bebas Perawatan
        Tahan hingga puluhan tahun
        Produk kami sangat cocok untuk bangunan yang banyak menggunakan material kayu (wood concept) karena dapat di aplikasikan sebagai Lantai (Decking), Dinding (Wall), Partisi/Penyekat ruangan (Partition), Plafon (Ceiling) serta dapat  dipakai sebagai material dekorasi untuk Pintu Pagar (Fencing).</p>
